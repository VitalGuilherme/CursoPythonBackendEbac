# Dado uma string, retorne o número de segmentos na string.
# Um segmento é definido como uma sequência contígua de caracteres não espacial.

minha_string = "Hello my name is john !!"

# comando split repara os segmentos.
meu_array = minha_string.split()
print(meu_array)
# comando len retorna o número de segmentos na string.
print(len(meu_array))