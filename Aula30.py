# Dado uma string s consistindo apenas nos caracteres 'a' e 'b',
# retorno verdadeiro se cada 'a' aparecer antes de cada 'b' na string.
# Caso contrário, retorne falso.

minha_string = "ababaaaababa"

if 'ba' not in minha_string:
    print("Está certo! Seguindo a regra")
else:
    print("WRONG!!!!! ERRADO!!!")